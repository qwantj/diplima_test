import re

def polish_formulas_v3(file_path):
    with open(file_path, 'r', encoding='utf-8') as f:
        text = f.read()

    # Define replacements
    data = [
        (r'итерационно, минимизируя целевую функцию \(1\.1\):.*?γ и λ — гиперпараметры\)\.', 
         """итерационно, минимизируя целевую функцию:

$$ L(\\phi) = \\sum_i l(\\hat{y}_i, y_i) + \\sum_k \\Omega(f_k) \\eqno(1.1) $$

где $L(\\phi)$ – целевая функция;
$l(\\hat{y}_i, y_i)$ – функция потерь (логистическая для бинарной классификации «атака»/«норма»);
$\\Omega(f_k)$ – регуляризационный член ($T$ – количество листьев, $w$ – веса листьев, $\\gamma$ и $\\lambda$ – гиперпараметры)."""),
        
        (r'На каждой итерации t добавляется новое дерево fₜ, минимизирующее приближение целевой функции \(1\.2\):.*?регуляризационный член для текущего дерева\.',
         """На каждой итерации $t$ добавляется новое дерево $f_t$, минимизирующее приближение целевой функции:

$$ L^{(t)} \\approx \\sum_i [g_i f_t(x_i) + \\frac{1}{2} h_i f_t^2(x_i)] + \\Omega(f_t) \\eqno(1.2) $$

где $L^{(t)}$ – приближение целевой функции на итерации $t$;
$g_i = \\partial l / \\partial \\hat{y}$ – градиент (первая производная) функции потерь;
$h_i = \\partial^2 l / \\partial \\hat{y}^2$ – гессиан (вторая производная) функции потерь;
$\\Omega(f_t)$ – регуляризационный член для текущего дерева."""),
        
        (r'каждый признак нормализуется методом стандартизации \(z-score\) по формуле \(1\.3\):.*?стандартное отклонение i-го признака\.',
         """каждый признак нормализуется методом стандартизации (z-score) по формуле:

$$ x'_i = (x_i - \\mu_i) / \\sigma_i \\eqno(1.3) $$

где $x'_i$ – нормализованное значение $i$-го признака;
$x_i$ – исходное значение признака;
$\\mu_i$ – среднее значение $i$-го признака на обучающей выборке;
$\\sigma_i$ – стандартное отклонение $i$-го признака.""")
    ]

    for pattern, replacement in data:
        # Use a lambda for replacement to avoid escape sequence parsing
        text = re.sub(pattern, lambda m: replacement, text, flags=re.DOTALL)

    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(text)

polish_formulas_v3('docs/academic/ВКР_черновик_claude.md')
print('Success.')
