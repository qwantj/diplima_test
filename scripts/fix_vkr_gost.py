# 2. Tables: Таблица N — Name -> Right-aligned number, Centered title
def fix_table(match):
    num = match.group(1)
    name = match.group(2).strip()
    return f'\n<p align="right">Таблица {num}</p>\n<p align="center"><b>{name}</b></p>\n'

